package MathsOperation;

public class MathsOperation1 {
    public void add(int a, int b) {
		int c = a+b;
		System.out.println("Addition of " + a + " and " + b + " is: " + c);
	}
	public void sub(int a, int b) {
		int c = a-b;
		System.out.println("Subtraction of " + a + " and " + b + " is: " + c);
	}
}
