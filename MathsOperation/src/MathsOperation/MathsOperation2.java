package MathsOperation;

public class MathsOperation2 {
    public void mul(int a, int b) {
		int c = a*b;
		System.out.println("Multiplication of " + a + " and " + b + " is: " + c);
	}
	public void div(int a, int b) {
		int c = a/b;
		System.out.println("Division of " + a + " and " + b + " is: " + c);
	}
}
